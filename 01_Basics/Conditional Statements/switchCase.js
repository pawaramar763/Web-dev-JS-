let color = 'black';

switch(color){
    case 'red':
        console.log(`stop`);
    break;

    case 'yellow':
        console.log(`go`);
    break;

    case 'green':
        console.log(`go fast`);
    break;

    default:
        console.log(`problem with traffic`);
}


